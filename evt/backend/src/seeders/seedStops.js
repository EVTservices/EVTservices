async function seedStops() {
  try {
    const routes = await Route.findAll();
    const routeMap = {};
    routes.forEach(route => {
      routeMap[route.route_name] = route.route_id;
    });

    await sequelize.transaction(async (t) => {
      for (const group of stopData) {
        const route_id = routeMap[group.route_name];
        if (!route_id) {
          console.warn(`⚠️ Route not found: ${group.route_name}`);
          continue;
        }

        const stopsToCreate = group.stops.map(name => ({
          name,
          route_id
        }));

        await Stop.bulkCreate(stopsToCreate, { transaction: t });
        console.log(`✅ Stops for "${group.route_name}" seeded!`);
      }
    });

    console.log("🎉 All stops seeded successfully!");
  } catch (error) {
    console.error("❌ Error seeding stops:", error);
  }
}

