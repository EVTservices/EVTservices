async function seedStops() {
  try {
    const routes = await Route.findAll();

    // 🔑 Use both route_name + shift_name as key
    const routeMap = {};
    routes.forEach(route => {
      const key = `${route.route_name}_${route.shift_name}`;
      routeMap[key] = route.route_id;
    });

    await sequelize.transaction(async (t) => {
      for (const group of stopData) {
        const key = `${group.route_name}_${group.shift_name}`;
        const route_id = routeMap[key];

        if (!route_id) {
          console.warn(`⚠️ Route not found: ${group.route_name} (${group.shift_name})`);
          continue;
        }

        const stopsToCreate = group.stops.map(name => ({
          name,
          route_id
        }));

        await Stop.bulkCreate(stopsToCreate, { transaction: t });
        console.log(`✅ Stops for "${group.route_name}" (${group.shift_name}) seeded!`);
      }
    });

    console.log("🎉 All stops seeded successfully!");
  } catch (error) {
    console.error("❌ Error seeding stops:", error);
  }
}

